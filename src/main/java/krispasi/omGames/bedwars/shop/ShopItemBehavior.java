package krispasi.omGames.bedwars.shop;

/**
 * Behavior tags that control how purchases are applied.
 * <p>Used by {@link krispasi.omGames.bedwars.game.GameSession} to grant items, upgrades,
 * or special handling for tools and armor.</p>
 */
public enum ShopItemBehavior {
    BLOCK,
    SWORD,
    BOW,
    CROSSBOW,
    ARMOR,
    PICKAXE,
    AXE,
    SHEARS,
    SHIELD,
    POTION,
    UPGRADE,
    UTILITY
}
